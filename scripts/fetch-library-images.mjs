// Fetch Wikipedia thumbnails for entries that need it
const wikiEntries = libraries.filter(([, hardcoded]) => !hardcoded);
const titles = wikiEntries.map(([,, wiki]) => wiki).join('|');
const url = `https://en.wikipedia.org/w/api.php?action=query&titles=${encodeURIComponent(titles)}&prop=pageimages&pithumbsize=960&format=json&pilicense=any`;

console.log('Fetching library images from Wikipedia API...');
const res = await fetch(url, {
  headers: { 'User-Agent': 'HailToTheChief/1.0 (library-image-fetch; educational project)' }
});
const data = await res.json();

if (!data.query?.pages) {
  console.error('API error:', JSON.stringify(data, null, 2));
  process.exit(1);
}

const thumbByTitle = {};
for (const page of Object.values(data.query.pages)) {
  thumbByTitle[page.title] = page.thumbnail?.source ?? null;
}
if (data.query.normalized) {
  for (const { from, to } of data.query.normalized) {
    if (thumbByTitle[to] !== undefined) thumbByTitle[from] = thumbByTitle[to];
  }
}
if (data.query.redirects) {
  for (const { from, to } of data.query.redirects) {
    if (thumbByTitle[to] !== undefined) thumbByTitle[from] = thumbByTitle[to];
  }
}

const sqlLines = [];
for (const [dbName, hardcoded, wikiTitle] of libraries) {
  const thumb = hardcoded ?? (wikiTitle ? thumbByTitle[wikiTitle] : null);
  if (thumb) {
    const escaped = dbName.replace(/'/g, "''");
    sqlLines.push(`UPDATE presidential_locations SET image_url = '${thumb}' WHERE name = '${escaped}';`);
    console.log(`✓ ${dbName.substring(0, 55)}`);
  } else {
    console.log(`✗ ${dbName.substring(0, 55)} — no image`);
    sqlLines.push(`-- No image found for: ${dbName}`);
  }
}

const sql = [
  '-- Library building image URLs fetched via Wikipedia action API (prop=pageimages, 960px)',
  '-- Generated by scripts/fetch-library-images.mjs',
  '',
  ...sqlLines,
  '',
].join('\n');

writeFileSync('supabase/migrations/20260101000007_library_image_urls.sql', sql);
console.log('\nWrote supabase/migrations/20260101000007_library_image_urls.sql');
console.log(`Updated: ${sqlLines.filter(l => l.startsWith('UPDATE')).length}/15`);
